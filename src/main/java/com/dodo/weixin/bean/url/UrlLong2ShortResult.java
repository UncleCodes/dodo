public class UrlLong2ShortResult extends ResultMsgBean{
	private String short_url;

	public String getShort_url() {
		return short_url;
	}

	public void setShort_url(String short_url) {
		this.short_url = short_url;
	}

	@Override
	public String toString() {
		return super.toString()+"\nUrlLong2ShortBean [short_url=" + short_url + "]";
	}
}
